<strong>git log：</strong> 查看提交日志。
<strong>git status：</strong> 查看工作区和暂存区的状态。
<strong>git diff：</strong> 查看工作区、暂存区或提交之间的差异。
<strong>git reset：</strong> 恢复或取消提交。
<strong>git remote：</strong> 管理远程仓库。
<strong>git fetch：</strong> 从远程仓库获取最新的更改，但不合并到本地分支。
<strong>git tag：</strong> 创建、列出或删除标签。</p>
